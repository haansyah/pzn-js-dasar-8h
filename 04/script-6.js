// Ternary Operation
/* if sederhana, ternary operator  terdiri dari kondisi
yang dievaluasi, jika menghasilkan true maka nilai pertama
diambil, jika false maka nilai kedua diambil*/

const nilai = 74;
let ucapan = nilai >= 75 ? "Selamat Anda Lulus" : "Silahkan coba lagi";

document.writeln(ucapan);
