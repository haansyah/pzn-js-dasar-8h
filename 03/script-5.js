// If Expression

const examValue = 81;

if (examValue > 90) {
  document.writeln("<h1>Good Job! A+</h1>");
} else if (examValue > 80) {
  document.writeln("<h1>Good Job! B</h1>");
} else if (examValue > 70) {
  document.writeln("<h1>Good Job! C</h1>");
} else {
  document.writeln("<h1>Try Again Next Year! D</h1>");
}
